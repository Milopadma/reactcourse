import React from "react";
import { connect } from "react-redux";
import { selectSong } from "../actions";

class SongList extends React.Component {
  renderList() {
    return this.props.songs.map((song) => {
      return (
        <div className="item" key={song.title}>
          <div className="right floated content">
            <button
              onClick={() => this.props.selectSong(song)}
              className="ui button primary"
            >
              Select
            </button>
          </div>
          <div className="content">{song.title}</div>
        </div>
      );
    });
  }
  render() {
    return <div className="ui divided list">{this.renderList()}</div>;
  }
}

const mapStateToProps = (state) => {
  // passes the global redux store state to this component as props
  // state is the global state (in redux store)
  return {
    songs: state.songs,
  };
};

export default connect(mapStateToProps, { selectSong: selectSong })(SongList); // connect() is a higher order component that returns a component that is connected to the redux store.
